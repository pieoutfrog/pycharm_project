def sum_of_numbers():
    a = int(input("Введите первое число"))
    b = int(input("Введите второе число"))
    sum_of_num = a + b
    return sum_of_num

def sub_of_numbers():
    a = int(input("Введите первое число"))
    b = int(input("Введите второе число"))
    sum_of_num = a - b
    return sum_of_num
